// Create an array with 4 fruits and console.log all names via
// a for loop

const fruits = ['Banana', 'Apple', 'Orange', 'Pineapple', 'Mango'];
for( let i = 0; i < fruits.length; i++ ){
	console.log(fruits[i]);
}

for( let i = fruits.length-1; i >= 0; i-- ){
	console.log(fruits[i]);
}